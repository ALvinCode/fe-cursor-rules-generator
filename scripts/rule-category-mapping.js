export function categorizeRuleByName(ruleName) {
  // 移除 .cursorrules 扩展名（如果有）
  const name = ruleName.replace(/\.cursorrules$/, '').toLowerCase();
  
  // 遍历所有类别，查找匹配
  for (const [category, rules] of Object.entries(RULE_CATEGORY_MAPPING)) {
    for (const [key, displayName] of Object.entries(rules)) {
      // 精确匹配或包含匹配
      if (name === key || name.includes(key) || key.includes(name)) {
        return category;
      }
    }
  }
  
  // 如果精确匹配失败，使用关键词匹配
  const lower = name.toLowerCase();
  
  // Frontend
  if (lower.includes('react') || lower.includes('vue') || lower.includes('angular') || 
      lower.includes('svelte') || lower.includes('nextjs') || lower.includes('next.js') ||
      lower.includes('astro') || lower.includes('solid') || lower.includes('qwik')) {
    return 'frontend';
  }
  
  // Backend
  if (lower.includes('nodejs') || lower.includes('node.js') || lower.includes('express') ||
      lower.includes('fastapi') || lower.includes('django') || lower.includes('flask') ||
      lower.includes('rails') || lower.includes('laravel') || lower.includes('spring') ||
      lower.includes('go') || lower.includes('php') || lower.includes('java') ||
      lower.includes('python') && !lower.includes('testing')) {
    return 'backend';
  }
  
  // Mobile
  if (lower.includes('react-native') || lower.includes('expo') || lower.includes('flutter') ||
      lower.includes('swift') || lower.includes('kotlin') || lower.includes('android') ||
      lower.includes('ios') || lower.includes('uikit')) {
    return 'mobile';
  }
  
  // Styling
  if (lower.includes('tailwind') || lower.includes('css') || lower.includes('styled-components') ||
      lower.includes('chakra') || lower.includes('mui') || lower.includes('material-ui')) {
    return 'styling';
  }
  
  // State Management
  if (lower.includes('redux') || lower.includes('mobx') || lower.includes('zustand') ||
      lower.includes('recoil') || lower.includes('pinia') || lower.includes('vuex') ||
      lower.includes('react-query')) {
    return 'state';
  }
  
  // Database
  if (lower.includes('mongodb') || lower.includes('postgresql') || lower.includes('mysql') ||
      lower.includes('supabase') || lower.includes('firebase') || lower.includes('graphql') ||
      lower.includes('axios') || lower.includes('apollo')) {
    return 'database';
  }
  
  // Testing
  if (lower.includes('jest') || lower.includes('vitest') || lower.includes('cypress') ||
      lower.includes('playwright') || lower.includes('puppeteer') || lower.includes('testing') ||
      lower.includes('test') || lower.includes('qa') || lower.includes('testrail') ||
      lower.includes('xray') || lower.includes('detox')) {
    return 'testing';
  }
  
  // Hosting
  if (lower.includes('netlify') || lower.includes('vercel') || lower.includes('aws') ||
      lower.includes('azure') || lower.includes('gcp') || lower.includes('cloudflare') ||
      lower.includes('heroku') || lower.includes('docker') || lower.includes('kubernetes')) {
    return 'hosting';
  }
  
  // Build Tools
  if (lower.includes('webpack') || lower.includes('vite') || lower.includes('rollup') ||
      lower.includes('esbuild') || lower.includes('swc') || lower.includes('turbo') ||
      lower.includes('nx') || lower.includes('chrome-extension') || lower.includes('vscode-extension') ||
      lower.includes('tauri') || lower.includes('electron') || lower.includes('github') ||
      lower.includes('git') || lower.includes('kubernetes')) {
    return 'build';
  }
  
  // Language-Specific
  if (lower.includes('typescript') || lower.includes('javascript') || lower.includes('python') ||
      lower.includes('java') || lower.includes('go') || lower.includes('rust') ||
      lower.includes('c++') || lower.includes('csharp') || lower.includes('php') ||
      lower.includes('ruby') || lower.includes('swift') || lower.includes('kotlin') ||
      lower.includes('dart') || lower.includes('solidity') || lower.includes('r ') ||
      lower.includes('pandas') || lower.includes('pytorch')) {
    return 'language';
  }
  
  // Default to other
  return 'other';
}

